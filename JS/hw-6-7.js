// Задание 11 Создайте функцию, которая принимает на вход массив целых чисел, а возвращает массив квадратов этих чисел.

function getSquares(arr) {// Функция для вычисления квадратов
    return arr.map(num => num ** 2); //новый массив с квадратами элементов
}
// Например 
let numbers = [1, 2, 3, 4, 5];
let squares = getSquares(numbers);

console.log("Исходный массив:", numbers); // [1, 2, 3, 4, 5]
console.log("Массив квадратов:", squares); // [1, 4, 9, 16, 25]

// Задание 12 Создайте функцию, которая принимает на вход массив строк, а возвращает массив длины слов.
function getWordLengths(words) {
    return words.map(word => word.length); // Возвращаем новый массив с длиной каждой строки
}

// Например 
let strings = ["apple", "banana", "cherry"];
let lengths = getWordLengths(strings);

console.log("Исходный массив строк:", strings); 
console.log("Массив длины строк:", lengths); 

// Задание 13 Создайте функцию, которая принимает на вход массив целых чисел, 
// а возвращает массив, содержащий только отрицательные значения.
// Функция для получения массива отрицательных чисел
function getNegativeNumbers(numbers) {
    return numbers.filter(number => number < 0); // Фильтруем только отрицательные числа
}

// Например
let array = [1, -2, 3, -4, 5, -6];
let negativeNumbers = getNegativeNumbers(array);

console.log("Исходный массив:", array); // [1, -2, 3, -4, 5, -6]
console.log("Массив отрицательных чисел:", negativeNumbers); // [-2, -4, -6]

