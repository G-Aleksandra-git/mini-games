// Задание 15 Создайте массив, состоящий из 6 элементов. Элементы массива необходимо сгенерировать с помощью 
// Math.random() в диапазоне от 1 до 10. 
// Требуется найти среднее арифметическое этих цифр, результат программы вывести в консоль.

let numbers = Array.from({ length: 6 }, () => Math.floor(Math.random() * 10) + 1);
let sum = numbers.reduce((acc, curr) => acc + curr, 0);//  сумма элементов массива
let average = sum / numbers.length; //среднее арифметическое

console.log("Массив чисел:", numbers);
console.log("Среднее арифметическое:", average);


